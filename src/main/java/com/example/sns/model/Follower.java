package com.example.sns.model;

public class Follower {
    private int id;
    private String fan;
    private String star;

    public int getId() {
        return this.id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getFan() {
        return this.fan;
    }

    public void setFan(String fan) {
        this.fan = fan;
    }

    public String getStar() {
        return this.star;
    }

    public void setStar(String star) {
        this.star = star;
    }

}
